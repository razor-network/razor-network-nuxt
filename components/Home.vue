import { setInterval, setTimeout } from "timers";
export default {
  name: "container",
  components: {
    FAQ,
    Team,
    Investors,
    Advisors
  },
  data() {
    return {
      text: "Finance",
      dataText: [
        "Finance",
        "Prediction markets",
        "Insurance",
        "Synthetic assets",
        "Stablecoins",
        "Identity",
        "Atomic swaps",
        "Finance",
        "Lending"
      ],
      i: 0,
      classes: ""
    };
  },
  methods: {
    change: function() {
      const that = this;
      setInterval(() => {
        if (that.i == 8) {
          that.i = 0;
          that.text = that.dataText[that.i];
        } else {
          that.i = that.i + 1;
          that.text = that.dataText[that.i];
        }
        that.classes = "fadeInDown";
      }, 1600);
    },
    changeClass: function() {
      const that = this;
      setInterval(() => {
        that.classes = "fadeOutDown";
      }, 1600);
    }
  },
  mounted: function() {
    this.changeClass();
    setTimeout(() => {
      this.change();
    }, 600);
  }
};
